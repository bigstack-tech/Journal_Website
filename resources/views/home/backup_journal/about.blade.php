        <h2 style="color: orange">ETHICAL CONSIDERATIONS</h2>
        <p>
            <b>The Editor</b> is required to act in a fair, balanced, objective manner and no discrimination of any sort is permitted or acceptable in any form or shape.All articles are required to considered if they align with the scope of the journal and are accepted solely on their academic merit and without commercial influence, after double blind peer review. All authors have a reasonable opportunity to respond to any complaints.
        </p>
        <p>
            <b>Reviewers</b> are appointed based on their expertise, to contribute to the decision-making process, thereby assist in improving the quality of the published article by reviewing the manuscript objectively and timeously. Reviewers are to maintain the confidentiality of any information supplied by the editor or author and may not retain or copy the manuscript in any way for any reason.The reviewers are expected to alert the editor or managing editor of any published or submitted content that is significantly similar to that under current review. Reviewers must state any concerns about conflicts of interest and alert the editor to these, if necessary withdrawing their services for reviewing a particular manuscript, which for example be from the same institution as the one in which they are employed
        </p>
        <p>
            <b>The Authors</b> are required to retain accurate records of data associated with their submitted manuscript, supply or provide access to any data referred to in an article, if requested request to do so. No article submitted may be under consideration or accepted for publication anywhere else. All sources must be cited accurately. The author/s must confirm that all the work in a submitted manuscript is original in nature and to acknowledge and cite content reproduced from other sources used. They also need to acquire permission to replicate any content from other sources. Authors must ensure that any studies involving human or animal subjects conforms to national, local and institutional laws and requirements (e.g. WMA Declaration of Helsinki,) and confirm that approval has been sought and obtained where appropriate from university research ethics committees or other legal bodies. Authors must ensure that the well-being, safety and protection of persons involved in the research, where applicable, is of primary concern. Potential conflicts of interest must be disclosed. Where errors may occur in publication, the author/s should notify the editor and necessary revisions will be made and an erratum published. Where deemed necessary, an article may be retracted.
            <br>
            Misconduct and unethical behaviour, if and when identified, must be brought to the attention of the editor and publisher. Minor misconduct will be dealt with rapidly and a ‘guilty’ party can respond electronically to establish their case in a collegial fashion. Any informant should also be collegial and provide sufficient information and evidence of error or intentional oversight, in order for an investigation to be initiated. All allegations are taken seriously and treated in the same way, until a successful decision or conclusion is reached. Evidence must be presented and no allegations will be made beyond those who need to know. The editor may consult with experts for advice and his/her decision will be binding. Where there has been misconduct, there will be a formal notice detailing the misconduct and the outcome thereof.
        </p>
